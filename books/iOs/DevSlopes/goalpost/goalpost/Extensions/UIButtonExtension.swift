import UIKit

extension UIButton {
    
    func setSelectedColor()
    {
        self.backgroundColor = #colorLiteral(red: 0.4451893568, green: 0.6960539222, blue: 0.4141852558, alpha: 1)
    }
    func setDeselectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.6688197255, green: 0.7978375554, blue: 0.667822659, alpha: 1)
    }
}
