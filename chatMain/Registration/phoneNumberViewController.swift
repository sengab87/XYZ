    func generateRandomPass()
    {
        var password = ""
        let token = UserDefaults.standard.string(forKey: "token")
        let upperCaseCount = upperCaseString.count
        let lowerCaseCount = lowerCaseString.count
        let specialCount = specialCharacter.count
        let tokenLength = token?.count
        for _ in 0...1
        {
            let loc = Int(arc4random_uniform(UInt32(upperCaseCount)))
            let charac = upperCaseString.index(upperCaseString.startIndex, offsetBy: loc)
            password.append(upperCaseString[charac])
            print(password)
        }
        for _ in 0...1
        {
            let loc = Int(arc4random_uniform(UInt32(lowerCaseCount)))
            let charac = lowerCaseString.index(lowerCaseString.startIndex, offsetBy: loc)
            password.append(lowerCaseString[charac])
            print(password)
        }
        for _ in 0...5
        {
            let loc = Int(arc4random_uniform(UInt32(tokenLength!)))
            let charac = token?.index((token?.startIndex)!, offsetBy: loc)
            password.append(token![charac!])
            print(password)
        }
        for _ in 0...2
        {
            let loc = Int(arc4random_uniform(UInt32(specialCount)))
            let charac = specialCharacter.index(specialCharacter.startIndex, offsetBy: loc)
            password.append(specialCharacter[charac])
            print(password)
        }
        UserDefaults.standard.set(password, forKey: "password")
        XMPPTest.shared.connect()
        self.performSegue(withIdentifier: "code", sender: self)
    }

